public class IfElseCaseThree
{
	public static void main(String[]args)
	{
	    System.out.println("Start of Main Method");
	    int i = 15;
	    int j = 14;
	    //if(i=j)
	    if(i == j)
	    {
	    	System.out.println("You are inside if block");
	    	System.out.println("You are going from if block");
	    }//close of if block
	    else 
	    {
            System.out.println("Start of else block");
            System.out.println("End of else block");

	    }//close of else block
	    System.out.println("End of Main Method");
	}//close of Main Method
}//close of class